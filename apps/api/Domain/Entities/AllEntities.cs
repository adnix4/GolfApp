// ─────────────────────────────────────────────────────────────────────────────
// EMAIL TEMPLATE
// Maps to the "email_templates" table.
// Templates are org-branded and stored in the database (not as static files).
// This allows organizers to customise email copy in the admin dashboard.
// ─────────────────────────────────────────────────────────────────────────────
[Table("email_templates")]
public class EmailTemplate
{
    [Column("id")]
    public Guid Id { get; set; }

    [Column("org_id")]
    public Guid OrgId { get; set; }

    [Column("trigger_type")]
    public EmailTriggerType TriggerType { get; set; }

    [Column("subject")]
    [MaxLength(200)]
    public string Subject { get; set; } = string.Empty;

    /// <summary>
    /// Full HTML email body with theme placeholders like {{PRIMARY_COLOR}}.
    /// At send time, buildEmailVars() from @gfp/theme substitutes the org's
    /// actual hex values. The QR code and player-specific data are injected
    /// by the Email feature service at send time.
    /// </summary>
    [Column("html_body")]
    public string HtmlBody { get; set; } = string.Empty;

    /// <summary>
    /// If false, this template is disabled — the trigger fires but no email is sent.
    /// Allows orgs to opt out of specific automated emails (e.g. disable reminder emails).
    /// </summary>
    [Column("is_active")]
    public bool IsActive { get; set; } = true;

    [ForeignKey(nameof(OrgId))]
    public Organization Organization { get; set; } = null!;
}

// ─────────────────────────────────────────────────────────────────────────────
// QR CODE
// Maps to the "qr_codes" table.
// Pre-generated QR codes for an event (included in the Print Kit PDF).
// ─────────────────────────────────────────────────────────────────────────────
[Table("qr_codes")]
public class QrCode
{
    [Column("id")]
    public Guid Id { get; set; }

    [Column("event_id")]
    public Guid EventId { get; set; }

    [Column("qr_type")]
    public QrType QrType { get; set; }

    /// <summary>
    /// Signed token encoded in the QR. Format depends on qr_type:
    ///   player_checkin → HMAC-signed JSON { type, player_id, event_id }
    ///   event_join     → just the event_code (public, no signature needed)
    ///   donation        → just the event_code
    /// Max 128 chars — QR codes degrade in density above this.
    /// </summary>
    [Column("token")]
    [MaxLength(128)]
    public string Token { get; set; } = string.Empty;

    /// <summary>
    /// SVG string of the rendered QR code image.
    /// Stored in the DB so the Print Kit PDF can be generated without
    /// re-rendering QR codes on every download.
    /// </summary>
    [Column("svg_data")]
    public string SvgData { get; set; } = string.Empty;

    [Column("created_at")]
    public DateTime CreatedAt { get; set; } = DateTime.UtcNow;

    [ForeignKey(nameof(EventId))]
    public Event Event { get; set; } = null!;
}
