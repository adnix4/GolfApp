// EMAIL TEMPLATE
// Maps to the "email_templates" table.
// Templates are org-branded and stored in the database (not as static files).
// This allows organizers to customise email copy in the admin dashboard.
// ─────────────────────────────────────────────────────────────────────────────
[Table("email_templates")]
public class EmailTemplate
{
    [Column("id")]
    public Guid Id { get; set; }

    [Column("org_id")]
    public Guid OrgId { get; set; }

    [Column("trigger_type")]
    public EmailTriggerType TriggerType { get; set; }

    [Column("subject")]
    [MaxLength(200)]
    public string Subject { get; set; } = string.Empty;

    /// <summary>
    /// Full HTML email body with theme placeholders like {{PRIMARY_COLOR}}.
    /// At send time, buildEmailVars() from @gfp/theme substitutes the org's
    /// actual hex values. The QR code and player-specific data are injected
    /// by the Email feature service at send time.
    /// </summary>
    [Column("html_body")]
    public string HtmlBody { get; set; } = string.Empty;

