// ─────────────────────────────────────────────────────────────────────────────
// Domain/Entities/AllEntities.cs — EF Core Entity Classes (Phase 1 Schema)
// ─────────────────────────────────────────────────────────────────────────────
//
// WHY ALL ENTITIES IN ONE FILE FOR NOW:
//   Phase 1 has 12 entity classes. Splitting them across files adds navigation
//   overhead. Once the team grows, split into one file per entity. For now,
//   keeping them together makes the spec → code mapping easier to verify.
//
// EF CORE CONVENTIONS USED:
//   • Id property named "Id" → EF Core recognizes it as the primary key
//   • Navigation properties marked as virtual → enables lazy loading if needed
//   • Required strings use string (not string?) — nullable reference types are enabled
//   • Nullable columns use string? / DateTime? / int? etc.
//   • [Column] attributes override EF Core's default snake_case conversion
//
// UUID GENERATION STRATEGY (spec Foundation §4.1):
//   "All tables use UUID primary keys generated by the API (not DB serial)"
//   Ids are set in the API code before inserting, using Guid.NewGuid().
//   EF Core is told NOT to use database-generated values via:
//     builder.Property(e => e.Id).ValueGeneratedNever()
//   This is configured in ApplicationDbContext.OnModelCreating().
//
// FUTURE PHASE COLUMNS (spec Foundation §4):
//   "Columns populated in later phases are nullable until that phase is deployed."
//   Phase 4 columns (stripe_customer_id, has_payment_method) are included
//   as nullable properties so the schema is complete from Day 1. This prevents
//   breaking migrations when Phase 4 ships.
// ─────────────────────────────────────────────────────────────────────────────

using System.ComponentModel.DataAnnotations;
using System.ComponentModel.DataAnnotations.Schema;
using NetTopologySuite.Geometries;
