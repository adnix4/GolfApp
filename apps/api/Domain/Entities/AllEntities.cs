// Maps to the "donations" table.
// ─────────────────────────────────────────────────────────────────────────────
[Table("donations")]
public class Donation
{
    [Column("id")]
    public Guid Id { get; set; }

    [Column("event_id")]
    public Guid EventId { get; set; }

    [Column("donor_name")]
    [MaxLength(200)]
    public string DonorName { get; set; } = string.Empty;

    [Column("donor_email")]
    [MaxLength(254)]
    public string DonorEmail { get; set; } = string.Empty;

    /// <summary>
    /// Amount in US cents (integer) to avoid floating-point currency errors.
    /// e.g. $50.00 is stored as 5000.
    /// All money values in the system follow this convention.
    /// </summary>
    [Column("amount_cents")]
    public int AmountCents { get; set; }

    /// <summary>Whether a donation receipt email has been sent to this donor.</summary>
    [Column("receipt_sent")]
    public bool ReceiptSent { get; set; } = false;

    /// <summary>
    /// Phase 4: Stripe PaymentIntent ID for online donations processed via the donation widget.
    /// Null for Phase 1 (donations are recorded manually by the organizer).
    /// </summary>
    [Column("stripe_payment_intent_id")]
    [MaxLength(100)]
    public string? StripePaymentIntentId { get; set; }

    /// <summary>True for admin-generated test data; cleared when moving to Active.</summary>
    [Column("is_test")]
    public bool IsTest { get; set; } = false;

    [Column("created_at")]
    public DateTime CreatedAt { get; set; } = DateTime.UtcNow;

    [ForeignKey(nameof(EventId))]
    public Event Event { get; set; } = null!;
}

