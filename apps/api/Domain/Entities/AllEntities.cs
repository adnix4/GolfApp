// ── SEASON ────────────────────────────────────────────────────────────────────
[Table("seasons")]
public class Season
{
    [Column("id")]
    public Guid Id { get; set; }

    [Column("league_id")]
    public Guid LeagueId { get; set; }

    [Column("name")]
    [MaxLength(200)]
    public string Name { get; set; } = string.Empty;

    [Column("total_rounds")]
    public short TotalRounds { get; set; }

    [Column("start_date")]
    public DateOnly StartDate { get; set; }

    [Column("end_date")]
